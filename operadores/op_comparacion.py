#Operador EQUIVALENTE
print("Operador equivalente")
print("5 == 4? ", 5 == 4)
print("5 == 5? ", 5 == 5)

#Operador MAYOR QUE
print("Operador Mayor que")
print("5 > 4? ", 5 > 4)
print("5 > 8? ", 5 > 8)
#Operador MENOR QUE
print("Operador Menor que")
print("5 < 4? ", 5 < 4)
print("5 < 8? ", 5 < 8)
#Operador MAYOR/MENOR O IGUAL QUE
print("Operador Mayor/Menor o igual que")
print("5 <= 8? ", 5 <= 8)
print("5 >= 8? ", 5 >= 8)
print("5 <= 5? ", 5 <= 5)
print("5 <= 4? ", 5 <= 4)
print("5 >= 4? ", 5 >= 4)

#Operador DISTINTO de
print("Operador DISTINTO de")
print("5 != 4? ", 5 != 4)
print("3 != 3? ", 3 != 3)
